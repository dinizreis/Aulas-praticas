# Tuplas tambem eh uma estrutura de dados. DIferentemente da das listas, as Tuplas nao podem sofrer mudancas/alteracoes

# Equanto nas listas nos usamos os [], nas Tuplas sempre usamos os () segue o exemplo abaixo

coordenada = (6, 8, 70)
print(coordenada)

#____________________________________________________________

# Se eu tentar alterar a Tupla era nao funcionara. por exemplo:

coordenada[0] = 7   # Aqui eu estou pegando o primeiro item da minha Tupla coordenada e tentando alterar para 7. e obvio que nao dara certo.

#______________________________________________________________

# Mas, podemos exibir os item que contem dentro da nossa tupla:

print(coordenada[1])    # Com isso podemos ver os items da nossa tupla. dentro dos colchetes [] colocando o indece e ele ira retornar o valor que buscamos.

